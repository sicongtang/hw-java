package me.sicongtang.utils.guava.basic.caches;

import java.util.Date;

public class CacheValue {
	private Long count;
	private Date date;
	
	public Long getCount() {
		return count;
	}
	public void setCount(Long count) {
		this.count = count;
	}
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	
}
